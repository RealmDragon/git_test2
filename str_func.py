def to_uppercase(text):
  """Возвращает строку со всеми заглавными буквами.

  Args:
    text: Строка, которую нужно преобразовать.

  Returns:
    Строка с заглавными буквами.
  """
  return text.upper()

def capitalize_words(text):
  """Делает заглавными первые буквы каждого слова в строке.

  Args:
    text: Строка, которую нужно преобразовать.

  Returns:
    Строка с заглавными первыми буквами каждого слова.
  """
  return text.title()